            <h1 class="vox">Magnavox</h1>
            <p class="vox">Latin for "great voice", Magnavox is an electronics company whose predecessor was founded in Napa, California in 1911 by Peter L. Jensen and Edwin Pridham. The two had previously invented the moving-coil loudspeaker used in telephone receivers. In 1972, Magnavox produced the Magnavox Odysseyy&#0153, what is now considered the first home video game console ever. In 1974 they were acquired by Philips and have been a subsidiary ever since.</p>
