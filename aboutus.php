            <h1 class="sandAss">Sanders Associates, INC.</h1>
            <p class="sandAss">Originally based in Nashua, New Hampshire, Sanders Associates, INC. was founded by eleven scientists and engineers in 1951. All ex-employees of Raytheon, Sanders Associates went on to become a premier manufacturer of printed circuits throughout the 1960s and '70s. It was named after one of the original founders Royden C. Sanders Jr. In 1966, Ralph H. Baer, an engineer at Sanders Associates, lead a team who invented the Magnavox Odyssey&#0153, the first home video game console. For this, Baer was awarded the National Medal of Technology in 2006 by President George W. Bush. He passed in 2014 at the age of 92.<p>
